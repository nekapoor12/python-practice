"""
03_operators.py
This file demonstrates different types of operators in Python:
- Arithmetic
- Comparison
- Logical
- Assignment
- Membership
"""

# Arithmetic Operators
a = 10
b = 3

print("Arithmetic Operators")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)   # Floor division
print("a % b =", a % b)     # Modulus
print("a ** b =", a ** b)   # Power

print("-" * 40)

# Comparison Operators
x = 5
y = 10

print("Comparison Operators")
print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)

print("-" * 40)

# Logical Operators
p = True
q = False

print("Logical Operators")
print("p and q:", p and q)
print("p or q:", p or q)
print("not p:", not p)

print("-" * 40)

# Assignment Operators
n = 10
print("Assignment Operators")
print("n =", n)

n += 5
print("n += 5 ->", n)

n -= 3
print("n -= 3 ->", n)

n *= 2
print("n *= 2 ->", n)

print("-" * 40)

# Membership Operators
lst = [1, 2, 3, 4, 5]

print("Membership Operators")
print("3 in lst:", 3 in lst)
print("10 not in lst:", 10 not in lst)

print("Program finished successfully.")


